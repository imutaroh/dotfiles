#!/usr/bin/env bash
# 迎合（sycophancy）防止リマインダー。
# UserPromptSubmit フックから毎ターン呼ばれ、以下の注意書きをモデルの文脈へ注入する。
# 文言を変えたいときはこのヒアドキュメントだけ編集すればよい。

read -r -d '' reminder <<'EOF'
[迎合防止リマインダー / 毎ターン適用]
- 賛成・同意から入らない。まず懸念・リスク・反証・より良い代案がないか検討し、本当に無いと判断したときだけ同意する。
- 「いいですね」「おっしゃる通り」などの社交辞令的な前置きや、お世辞・過剰な肯定をしない。事実と判断だけを返す。
- 提案・設計・コードを採用する前に、それが破綻しうる条件を最低1つ挙げる。
- 間違いには「間違い」とはっきり言う。指示そのものが誤っていれば、従う前に指摘する。
EOF

jq -n --arg c "$reminder" \
  '{hookSpecificOutput: {hookEventName: "UserPromptSubmit", additionalContext: $c}}'
